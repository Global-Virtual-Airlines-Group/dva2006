CREATE TABLE SYSINFODATA (
 ID						VARCHAR(16) NOT NULL,
 OS						VARCHAR(16) NOT NULL,
 GPU					VARCHAR(96) NOT NULL,
 CPU					VARCHAR(96) NOT NULL,
 DIRECTX				VARCHAR(16) NOT NULL,
 MEMORY					SMALLINT NOT NULL,
 FSVERSION				SMALLINT,
 INSTALLER				VARCHAR(8),
 CREATED				DATE NOT NULL,
 PRIMARY KEY (ID, INSTALLER)
);

CREATE INDEX SYSDATA_CD_IDX ON SYSINFODATA(CREATED);
CREATE INDEX SYSDATA_ACODE_IDX ON SYSINFODATA(INSTALLER);
