CREATE TABLE INNOVATA_IMPORT (
 ID							INTEGER UNSIGNED NOT NULL,
 CARRIER					VARCHAR(3) NOT NULL,
 FLIGHT_NUM					INTEGER UNSIGNED NOT NULL,
 LEG						TINYINT UNSIGNED DEFAULT 1,
 EQTYPE						VARCHAR(6) NOT NULL,
 STARTDATE					DATE NOT NULL,
 ENDDATE					DATE NOT NULL,
 AIRPORT_D					CHAR(3) NOT NULL,
 AIRPORT_A					CHAR(3) NOT NULL,
 TIME_D						TIME NOT NULL,
 TIME_A						TIME NOT NULL,
 DAY_ADJUST					TINYINT,
 RESTRICT_CODE				CHAR(1),
 STOPS						TINYINT UNSIGNED,
 CODESHARE					BOOLEAN DEFAULT 0,
 CS_INFO					VARCHAR(155),
 CSHARE						VARCHAR(48),
 PRIMARY KEY (ID)
);
